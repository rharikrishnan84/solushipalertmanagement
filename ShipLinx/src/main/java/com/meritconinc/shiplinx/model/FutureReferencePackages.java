package com.meritconinc.shiplinx.model;

public class FutureReferencePackages {
	private Long futRefPackId;
	private Double packLength;
	private Double packWidth;
	private Double packHeight;
	private Double packWeight;
	private Double packCodAmount;
	private Double packInsuranceAmount;
	private String packDescription;
	private Long customerId;
	private Long futureReferenceId;
	
	
	public Long getFutRefPackId() {
		return futRefPackId;
	}
	public void setFutRefPackId(Long futRefPackId) {
		this.futRefPackId = futRefPackId;
	}
	public Double getPackLength() {
		return packLength;
	}
	public void setPackLength(Double packLength) {
		this.packLength = packLength;
	}
	public Double getPackWidth() {
		return packWidth;
	}
	public void setPackWidth(Double packWidth) {
		this.packWidth = packWidth;
	}
	public Double getPackHeight() {
		return packHeight;
	}
	public void setPackHeight(Double packHeight) {
		this.packHeight = packHeight;
	}
	public Double getPackWeight() {
		return packWeight;
	}
	public void setPackWeight(Double packWeight) {
		this.packWeight = packWeight;
	}
	public Double getPackCodAmount() {
		return packCodAmount;
	}
	public void setPackCodAmount(Double packCodAmount) {
		this.packCodAmount = packCodAmount;
	}
	public Double getPackInsuranceAmount() {
		return packInsuranceAmount;
	}
	public void setPackInsuranceAmount(Double packInsuranceAmount) {
		this.packInsuranceAmount = packInsuranceAmount;
	}
	public String getPackDescription() {
		return packDescription;
	}
	public void setPackDescription(String packDescription) {
		this.packDescription = packDescription;
	}
	public Long getCustomerId() {
		return customerId;
	}
	public void setCustomerId(Long customerId) {
		this.customerId = customerId;
	}
	public Long getFutureReferenceId() {
		return futureReferenceId;
	}
	public void setFutureReferenceId(Long futureReferenceId) {
		this.futureReferenceId = futureReferenceId;
	}
	
	

}
